package com.client.bean;

import java.io.Serializable;
import java.util.List;

/**
 * 访客答题请求 参数实体类
 * 
 * @author dingzhenzhen
 * @date 2019-05-23 18:38
 */
public class ReqExamBean implements Serializable {
	private static final long serialVersionUID = -3923128904751035424L;
	private String idcard;// 访客身份证号码
	private int userId;// 公司id
	private String qid;// 试卷id
	private String vphone;
	private String vemail;
	private String name;
	private int tid;
	private List<QuestionnaireDetail> reqDetList;// 试卷下题目列表

	public String getIdcard() {
		return idcard;
	}

	public void setIdcard(String idcard) {
		this.idcard = idcard;
	}

	public int getUserId() {
		return userId;
	}

	public void setUserId(int userId) {
		this.userId = userId;
	}

	public String getQid() {
		return qid;
	}

	public void setQid(String qid) {
		this.qid = qid;
	}

	public List<QuestionnaireDetail> getReqDetList() {
		return reqDetList;
	}

	public void setReqDetList(List<QuestionnaireDetail> reqDetList) {
		this.reqDetList = reqDetList;
	}

	public String getVphone() {
		return vphone;
	}

	public void setVphone(String vphone) {
		this.vphone = vphone;
	}

	public String getVemail() {
		return vemail;
	}

	public void setVemail(String vemail) {
		this.vemail = vemail;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getTid() {
		return tid;
	}

	public void setTid(int tid) {
		this.tid = tid;
	}
	
}
